function initDivinePanel() {
  _bvModal  = document.getElementById("bv-modal");
  _bvDialog = document.getElementById("bv-dialog");
  _bvBodyEl = document.getElementById("bv-modal-body");
  if (!_bvModal || !_bvBodyEl) return;

  document.getElementById("bv-modal-close").addEventListener("click", _closeBvModal);
  document.getElementById("bv-backdrop").addEventListener("click", _closeBvModal);

  _bvBodyEl.addEventListener("click", e => {
    const btn = e.target.closest("[data-bv-toggle]");
    if (btn) _toggleBvIntervention(btn.dataset.bvToggle);
  });

  document.addEventListener("keydown", e => {
    if (e.key === "Escape" && _bvModal && !_bvModal.hidden) {
      e.stopImmediatePropagation();
      _closeBvModal();
    }
  });

  const section = document.getElementById("panel-bv");
  if (section) {
    section.addEventListener("click", e => {
      if (e.target.closest("#btn-bv")) openBvModal();
      const removeBtn = e.target.closest("[data-bv-toggle]");
      if (removeBtn && !e.target.closest("#bv-modal-body")) _toggleBvIntervention(removeBtn.dataset.bvToggle);
    });
  }
}

document.addEventListener("DOMContentLoaded", initDivinePanel);
