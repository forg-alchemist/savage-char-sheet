let _bvModal  = null;
let _bvDialog = null;
let _bvBodyEl = null;

function _isBvSelected(name) {
  return (state.selectedDivineInterventions || []).includes(name);
}

function _toggleBvIntervention(name) {
  const list = state.selectedDivineInterventions || [];
  const idx = list.indexOf(name);
  if (idx === -1) {
    state.selectedDivineInterventions = [...list, name];
  } else {
    state.selectedDivineInterventions = list.filter(n => n !== name);
  }
  scheduleSave();
  _renderBvModalBody();
  _renderDivinePanelSelected();
}

function _bvCardHtml(bv) {
  const selected = _isBvSelected(bv.name);
  return `<div class="bv-modal-card" data-bv-name="${bv.name}">
    <div class="bv-card-main">
      <div class="bv-modal-card-name">${bv.name}</div>
      <div class="bv-stats-block">
        <div class="bv-stat-row">
          <span class="bv-stat-label">Модификатор веры</span>
          <span class="bv-stat-value bv-stat--modifier">${bv.modifier}</span>
        </div>
        <div class="bv-stat-row">
          <span class="bv-stat-label">Дистанция</span>
          <span class="bv-stat-value">${bv.distance}</span>
        </div>
        <div class="bv-stat-row">
          <span class="bv-stat-label">Длительность</span>
          <span class="bv-stat-value">${bv.duration}</span>
        </div>
        <div class="bv-stat-row">
          <span class="bv-stat-label">Проявления</span>
          <span class="bv-stat-value bv-stat--manifestations">${bv.manifestations}</span>
        </div>
      </div>
      <p class="bv-card-desc">${bv.description}</p>
      ${bv.variants ? `<div class="bv-variants-table">${bv.variants.map(v =>
        `<div class="bv-variant-row">
          <span class="bv-variant-name">${v.name}</span>
          <span class="bv-variant-text">${v.text}</span>
        </div>`).join("")}</div>` : ""}
    </div>
    <button type="button" class="bv-toggle-btn${selected ? " bv-toggle-btn--remove" : ""}" data-bv-toggle="${bv.name}">
      ${selected ? "УБРАТЬ ИЗ КАРТЫ" : "ДОБАВИТЬ В КАРТУ"}
    </button>
  </div>`;
}

function _renderBvModalBody() {
  if (!_bvBodyEl) return;
  _bvBodyEl.innerHTML = BV_DATA.map(_bvCardHtml).join("");
}

function openBvModal() {
  if (!_bvModal) return;
  _bvModal.hidden = false;
  requestAnimationFrame(() => _bvDialog.classList.add("open"));
  _renderBvModalBody();
}

function _closeBvModal() {
  _bvDialog.classList.remove("open");
  setTimeout(() => { _bvModal.hidden = true; }, 320);
}

function _renderDivinePanelSelected() {
  const section = document.getElementById("panel-bv");
  if (!section) return;
  const list = state.selectedDivineInterventions || [];
  let existing = section.querySelector(".bv-selected-list");
  if (list.length === 0) {
    if (existing) existing.remove();
    return;
  }
  const html = `<div class="bv-selected-list">${
    list.map(name => {
      const bv = BV_DATA.find(b => b.name === name);
      if (!bv) return "";
      const variantsHtml = bv.variants ? `<div class="bv-variants-table">${bv.variants.map(v =>
        `<div class="bv-variant-row">
          <span class="bv-variant-name">${v.name}</span>
          <span class="bv-variant-text">${v.text}</span>
        </div>`).join("")}</div>` : "";
      return `<div class="bv-selected-card">
        <button type="button" class="bv-selected-remove" data-bv-toggle="${bv.name}" title="Убрать">×</button>
        <div class="bv-selected-head">
          <span class="bv-selected-name">${bv.name}</span>
          <span class="bv-selected-mod">${bv.modifier}</span>
        </div>
        <div class="bv-selected-meta">${bv.distance} · ${bv.duration}</div>
        <p class="bv-selected-desc">${bv.description}</p>
        ${variantsHtml}
      </div>`;
    }).join("")
  }</div>`;
  if (existing) {
    existing.outerHTML = html;
  } else {
    const btn = section.querySelector("#btn-bv");
    if (btn) btn.insertAdjacentHTML("afterend", html);
  }
}

