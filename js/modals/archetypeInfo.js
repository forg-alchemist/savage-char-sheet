// ── Modal ─────────────────────────────────────────────────────────────────────

const _atypeModal    = document.getElementById('archetype-modal');
const _atypeBackdrop = _atypeModal.querySelector('.atype-backdrop');
const _atypeDialog   = _atypeModal.querySelector('.atype-dialog');
const _atypeNameEl   = _atypeModal.querySelector('.atype-name');
const _atypeSubEl    = _atypeModal.querySelector('.atype-subtitle');
const _atypeBodyEl   = _atypeModal.querySelector('.atype-body');

// ── Pager (multi-archetype navigation) ──
let _atypePages = [];
let _atypeCurrentPage = 0;
let _atypeMode = 'mine'; // 'mine' | 'all'

const _atypePager = document.createElement('div');
_atypePager.className = 'atype-pager';
_atypePager.hidden = true;
_atypePager.innerHTML =
  '<button type="button" class="atype-prev-btn" aria-label="Предыдущий архетип">‹</button>' +
  '<span class="atype-page-indicator"></span>' +
  '<button type="button" class="atype-next-btn" aria-label="Следующий архетип">›</button>';
_atypeSubEl.after(_atypePager);

const _atypePrevBtn       = _atypePager.querySelector('.atype-prev-btn');
const _atypeNextBtn       = _atypePager.querySelector('.atype-next-btn');
const _atypePageIndicator = _atypePager.querySelector('.atype-page-indicator');

// ── Mode toggle button ──
const _atypeModeBtn = document.createElement('button');
_atypeModeBtn.type = 'button';
_atypeModeBtn.className = 'atype-mode-btn';
_atypeModeBtn.textContent = 'ВСЕ АРХЕТИПЫ';
const _atypeHeader = _atypeModal.querySelector(".atype-header");
_atypeHeader.append(_atypeModeBtn);

_atypeModeBtn.addEventListener('click', () => {
  if (_atypeMode === 'mine') {
    _atypeMode = 'all';
    _atypeModeBtn.textContent = 'МОИ АРХЕТИПЫ';
    _atypePages = Object.keys(ARCHETYPE_INFO);
  } else {
    _atypeMode = 'mine';
    _atypeModeBtn.textContent = 'ВСЕ АРХЕТИПЫ';
    _atypePages = computeArchetypes();
  }
  _atypeRenderPage(0);
});

function _atypeRenderPage(idx) {
  _atypeCurrentPage = idx;
  const archetype = _atypePages[idx];
  const info = ARCHETYPE_INFO[archetype] || ARCHETYPE_INFO['Стрелок'];

  _atypeNameEl.textContent = archetype;
  _atypeSubEl.textContent  = info.subtitle || '';
  _atypeDialog.dataset.theme = info.theme || 'gunslinger';

  const cornerEls = _atypeModal.querySelectorAll('.atype-corner');
  const decos = info.corners || ['✦', '★', '✦', '★'];
  cornerEls.forEach((el, i) => { el.textContent = decos[i] || ''; });

  const artHtml = info.art
    ? `<img src="${info.art}" alt="${archetype}" class="atype-art-img" />`
    : '';

  _atypeBodyEl.innerHTML = artHtml + info.sections.map(s => {
    let html = '';
    if (s.title) html += `<h4 class="atype-section-title">${s.title}</h4>`;
    html += s.body;
    return html;
  }).join('');
  _atypeBodyEl.scrollTop = 0;

  if (_atypePages.length > 1) {
    _atypePageIndicator.textContent = `${idx + 1} / ${_atypePages.length}`;
    _atypePrevBtn.disabled = idx === 0;
    _atypeNextBtn.disabled = idx === _atypePages.length - 1;
    _atypePager.hidden = false;
  } else {
    _atypePager.hidden = true;
  }
}

_atypePrevBtn.addEventListener('click', () => {
  if (_atypeCurrentPage > 0) _atypeRenderPage(_atypeCurrentPage - 1);
});
_atypeNextBtn.addEventListener('click', () => {
  if (_atypeCurrentPage < _atypePages.length - 1) _atypeRenderPage(_atypeCurrentPage + 1);
});

function openArchetypeInfo() {
  _atypeMode = 'mine';
  _atypeModeBtn.textContent = 'ВСЕ АРХЕТИПЫ';
  _atypePages = computeArchetypes();
  _atypeRenderPage(0);
  _atypeModal.hidden = false;
  requestAnimationFrame(() => _atypeDialog.classList.add('open'));
}

function closeArchetypeInfo() {
  _atypeDialog.classList.remove('open');
  setTimeout(() => { _atypeModal.hidden = true; }, 280);
}

_atypeBackdrop.addEventListener('click', closeArchetypeInfo);
_atypeModal.querySelector('.atype-close').addEventListener('click', closeArchetypeInfo);

document.querySelector('.info-btn').addEventListener('click', openArchetypeInfo);

document.addEventListener('keydown', e => {
  if (e.key === 'Escape' && !_atypeModal.hidden) {
    e.stopImmediatePropagation();
    closeArchetypeInfo();
  }
});
